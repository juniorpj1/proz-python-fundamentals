# A função type() em Python informa o tipo da variável que está sendo inserida.
# Então, esse código mostra na tela, através do comando print(), os tipos das variáveis criadas,
# sendo apresentados em sequência int, float, string e boolean.

numero_inteiro = 5

numero_float = 5.7

texto = 'este é um texto'

booleano = False

print('Este é um tipo')

print(type(numero_inteiro))

print('Este é um tipo')

print(type(numero_float))

print('Este é um tipo')

print(type(texto))

print('Este é um tipo')

print(type(booleano))

